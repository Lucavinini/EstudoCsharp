
class Program
{
    static void Main()
    {

        //Operadores Aritmédicos
        Console.WriteLine(12 + 22);
        Console.WriteLine(2 - 2);
        Console.WriteLine(2 * 2);
        //Potenciação
        //Não existe nativamente, apenas com o uso de bibliotecas.
        Console.WriteLine(2 / 2);
        Console.WriteLine(2 % 4);



        //Operadores de Atribuição

        // (=) Atribuição simples

        int x = 30;

        x = 40;

        // Incremento
        x++;
        Console.WriteLine(x);

        //Atribuição composta, segue-se da operação que vai feito com um igual e um valor.

        x -= 1;
        Console.WriteLine(x);
        
        x--;

        Console.WriteLine(x);


    }
}